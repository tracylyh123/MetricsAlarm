<?php
namespace MetricsAlarm;

class User
{
    use AlarmableTrait;

    protected $id;

    protected $name;

    protected $email;

    public function __construct(string $id, string $name, string $email)
    {
        $this->id = $id;
        $this->name = $name;
        $this->email = $email;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function getEmail(): string
    {
        return $this->email;
    }
}
